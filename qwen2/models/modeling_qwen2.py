def rotate_half(x):
    """
    Rotates half the hidden dims of the input.
    
    x.shape =  (batch_size, seq_len, hidden_size)
    
    ## 原理：
    旋转操作
        -x2 对后半部分取负
        torch.cat((-x2, x1), dim=-1)
        将取负后的后半部分与前半部分拼接
        
    与旋转的关系
        这种操作类似于在复数空间中将向量旋转90度：
        如果将x1和x2看作复数的实部和虚部
        那么 -x2 + x1 就相当于将复数旋转90度
    """
    x1 = x[..., : x.shape[-1] // 2]
    x2 = x[..., x.shape[-1] // 2 :]
    return torch.cat((-x2, x1), dim=-1)


